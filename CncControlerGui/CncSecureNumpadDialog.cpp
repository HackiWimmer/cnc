#include "GlobalFunctions.h"
#include "CncSecureNumpadDialog.h"

///////////////////////////////////////////////////////////////////
CncSecureNumpadDialog::CncSecureNumpadDialog(wxWindow* parent, CncSecureNumpad::Type type, int precision, double min, double max)
: CncSecureNumpadDialogBase		(parent)
, numpad						(NULL)
///////////////////////////////////////////////////////////////////
{
	// numpad control
	numpad = new CncSecureNumpad(this, type, precision, min, max); 
	GblFunc::replaceControl(m_numpadPlaceholder, numpad);
}
///////////////////////////////////////////////////////////////////
CncSecureNumpadDialog::~CncSecureNumpadDialog() {
///////////////////////////////////////////////////////////////////
	wxDELETE( numpad );
}
///////////////////////////////////////////////////////////////////
void CncSecureNumpadDialog::onOk(wxCommandEvent& event) {
///////////////////////////////////////////////////////////////////
	EndModal(wxID_OK);
}
///////////////////////////////////////////////////////////////////
void CncSecureNumpadDialog::onCancel(wxCommandEvent& event) {
///////////////////////////////////////////////////////////////////
	EndModal(wxID_CANCEL);
}
///////////////////////////////////////////////////////////////////
void CncSecureNumpadDialog::onCloseWindow(wxCloseEvent& event) {
///////////////////////////////////////////////////////////////////
	EndModal(wxID_CANCEL);
}
