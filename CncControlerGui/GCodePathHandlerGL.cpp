#include "GCodePathHandlerGL.h"

//////////////////////////////////////////////////////////////////
GCodePathHandlerGL::GCodePathHandlerGL(CncGCodePreview* gl) 
: GCodePathHandlerBase()
, glControl(gl)
, currentSpeedMode(CncSpeedMode::CncSpeedRapid)
{
//////////////////////////////////////////////////////////////////
	wxASSERT(glControl);
	glControl->initColours();
}
//////////////////////////////////////////////////////////////////
GCodePathHandlerGL::~GCodePathHandlerGL() {
//////////////////////////////////////////////////////////////////
}
//////////////////////////////////////////////////////////////////
bool GCodePathHandlerGL::prepareWorkImpl() {
//////////////////////////////////////////////////////////////////
	wxASSERT(glControl);
	glControl->pushProcessMode();
	return true;
}
//////////////////////////////////////////////////////////////////
bool GCodePathHandlerGL::finishWorkImpl() {
//////////////////////////////////////////////////////////////////
	wxASSERT(glControl);
	glControl->popProcessMode();
	return true;
}
//////////////////////////////////////////////////////////////////
bool GCodePathHandlerGL::processLinearMove(bool alreadyRendered) {
//////////////////////////////////////////////////////////////////
	wxASSERT(glControl);
	static GLI::VerticeDoubleData vd;
	
	vd.setVertice(0L, currentSpeedMode, currentPos);
	glControl->appendVertice(vd);
	
	return true; 
}
//////////////////////////////////////////////////////////////////
bool GCodePathHandlerGL::changeCurrentFeedSpeedXYZ(CncSpeedMode sm, double value) {
//////////////////////////////////////////////////////////////////
	currentSpeedMode = sm;
	return true;
}
//////////////////////////////////////////////////////////////////
bool GCodePathHandlerGL::changeCurrentSpindleSpeed(double value) {
//////////////////////////////////////////////////////////////////
	return true;
}
//////////////////////////////////////////////////////////////////
bool GCodePathHandlerGL::initNextPath() {
//////////////////////////////////////////////////////////////////
	return true;
}

