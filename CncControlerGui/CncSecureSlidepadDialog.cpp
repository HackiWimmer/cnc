#include "GlobalFunctions.h"
#include "CncSecureSlidepadDialog.h"

/////////////////////////////////////////////////////////////////////
CncSecureSlidepadDialog::CncSecureSlidepadDialog(wxWindow* parent)
: CncSecureSlidepadDialogBase		(parent)
, slidepad							(NULL)
/////////////////////////////////////////////////////////////////////
{
	// slidepad control
	slidepad = new CncSecureSlidepad(this); 
	GblFunc::replaceControl(m_slidepadPlaceholder, slidepad);
}
/////////////////////////////////////////////////////////////////////
CncSecureSlidepadDialog::~CncSecureSlidepadDialog() {
/////////////////////////////////////////////////////////////////////
	wxDELETE( slidepad );
}
/////////////////////////////////////////////////////////////////////
void CncSecureSlidepadDialog::onOk(wxCommandEvent& event) {
/////////////////////////////////////////////////////////////////////
	EndModal(wxID_OK);
}
/////////////////////////////////////////////////////////////////////
void CncSecureSlidepadDialog::onCancel(wxCommandEvent& event) {
/////////////////////////////////////////////////////////////////////
	EndModal(wxID_CANCEL);
}
/////////////////////////////////////////////////////////////////////
void CncSecureSlidepadDialog::onCloseWindow(wxCloseEvent& event) {
/////////////////////////////////////////////////////////////////////
	EndModal(wxID_CANCEL);
}
